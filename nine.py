# Write program to Add, Subtract, Multiply, Divide 2 Complex numbers.


def add(a,b):

    return a + b




def sub(a,b):

    return a - b




def mul(a,b):

    return a * b




def div1(a,b):

    return a / b



print("Addition:", add(10 + 7j, 5 + 3j))

print("Substraction: ", sub(10 + 7j, 5 + 3j))

print("Multiplication: ", mul(10 + 7j, 5 + 3j))

print("Division: ", div1(10 + 7j, 5 + 3j))

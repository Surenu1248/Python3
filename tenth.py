# Using assignment operators, perform following operations
# Addition, Substation, Multiplication, Division, Modulus, Exponent and Floor division operations

def add(a,b):

    return a + b




def sub(a,b):

    return a - b




def mul(a,b):

    return a * b




def div1(a,b):

    return a / b




def div2(a,b):

    return a // b

def modulus(a,b):
    return a % b
    
def expo(a,b):
    return a ** b

print("Addition: ", add(10, 5))

print("Substraction: ", sub(10, 5))

print("Multiplication: ", mul(10, 5))

print("Floating Division: ", div1(10, 5))

print("Floor Division: ", div2(10, 5))

print("Modulus : ", modulus(10,4))

print("Exponential: ", expo(2,3))


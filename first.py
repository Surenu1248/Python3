# Write a program for add, subtract, multiplication, division of two numbers.

def add(a,b):

    return a + b




def sub(a,b):

    return a - b




def mul(a,b):

    return a * b




def div1(a,b):

    return a / b




def div2(a,b):

    return a // b


print(add(10, 5))

print(sub(10, 5))

print(mul(10, 5))

print("Floating Division: ", div1(10, 5))

print("Integer Division: ", div2(10, 5))
